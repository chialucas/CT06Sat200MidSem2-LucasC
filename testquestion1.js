function setup() {
    createCanvas(600,400);
    background(0);
    stroke("red");
    strokeWeight(10);
}

function draw() {
    fill("black");
    ellipse(150,200,100,100);
    fill("black");
    triangle(250,250,300,150,350,250);
    fill("black");
    rect(400,150,100,100);
}



